public class SepBranch {
    public static void main(String[] args) {
        int resul =+1;
        System.out.println(resul);// 1
        resul--;// 0
        System.out.println(resul);
        resul ++;//1
        System.out.println(resul);
        resul = -resul;//-1
        System.out.println(resul);
        boolean success = false;
        System.out.println(success);
        System.out.println(!success);
    }
}

